package ca.ivasenko.mobiletest.resources.model;

public class BusinessDay
{
    private String from;
    private String to;

    public BusinessDay()
    {
    }

    public String getFrom()
    {
        return from;
    }

    public void setFrom(String from)
    {
        this.from = from;
    }

    public String getTo()
    {
        return to;
    }

    public void setTo(String to)
    {
        this.to = to;
    }
}
